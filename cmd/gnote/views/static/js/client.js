// isEmpty check if username or password is empty
function isEmpty(username, password) {
  if (username == "" || password == "") {
    return true;
  } else {
    return false;
  }
}

function logout() {
  postData("/auth/logout", {}).then((data) => {
    window.location.href = "/";
  });
}
